//Declare Default Functions
UART_HandleTypeDef hlpuart1;
PCD_HandleTypeDef hpcd_USB_OTG_FS;
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
static void MX_LPUART1_UART_Init(void);
static void MX_USB_OTG_FS_PCD_Init(void);

int main(void)
{
  //Call default functions
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_LPUART1_UART_Init();
  MX_USB_OTG_FS_PCD_Init();

  //Initialize Keypad
  keypadPinInit();
  //Initialize DAC
  DAC_Init();

  //Declare DAC variables
  uint32_t inputVoltage = 0;
  uint32_t digitalVoltage = 0;
  uint32_t count = 0;
  uint32_t digit = 0;;

  while (1)
  {
		/* Call getKey function to continuously look for a key press and assign
		 it to the digit variable */
	  	digit = getKey();

	  		/*The keypad function returns -1 if no key is pressed. If a key is pressed,
	  		 the digit is collected to the inputVariable and shifted left one tens' place
	  		 with respect to the count for count up to 3. (ex. a press of 1,2,3 makes
	  		 inputVoltage 123.) This value is then sent to a function to be converted to the
	  		 digital voltage input to be sent to the DAC to produce this inputVoltage.
	  		 */
    	    if(digit != -1)
    	    {
				Delay();
				inputVoltage *= 10;
				inputVoltage += digit;
				Delay();
				count++;
				if(count == 3)
				{
					//If inputVoltage > the reference voltage, output the ref. voltage. (i.e3.3V)
					if (inputVoltage > 329)
					{
						digitalVoltage = MAX_VOLTAGE;
						count = 0;
						inputVoltage = 0;
					}
					//Cover edge case of inputVoltage = 000
					else if (inputVoltage == 0)
					{
						digitalVoltage = 0;
						count = 0;
						inputVoltage = 0;
					}
					else
					{
						digitalVoltage = DAC_Voltage_Conversion(inputVoltage);
						count= 0;
						inputVoltage = 0;
					}

				}
    	    }
    	    //Write the converted inputVoltage to the DAC
    	    DAC_Write(digitalVoltage);
  }
}

